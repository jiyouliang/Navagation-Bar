public class HotBuyInfo {
    private int resId;
    private String title;
    private String subTitle;
    private String att1;
    private String subAtt1;
    private String att2;
    private String subAtt2;
    private String price;

    public int getResId() {
        return resId;
    }

    public void setResId(int resId) {
        this.resId = resId;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getSubTitle() {
        return subTitle;
    }

    public void setSubTitle(String subTitle) {
        this.subTitle = subTitle;
    }

    public String getAtt1() {
        return att1;
    }

    public void setAtt1(String att1) {
        this.att1 = att1;
    }

    public String getSubAtt1() {
        return subAtt1;
    }

    public void setSubAtt1(String subAtt1) {
        this.subAtt1 = subAtt1;
    }

    public String getAtt2() {
        return att2;
    }

    public void setAtt2(String att2) {
        this.att2 = att2;
    }

    public String getSubAtt2() {
        return subAtt2;
    }

    public void setSubAtt2(String subAtt2) {
        this.subAtt2 = subAtt2;
    }

    public String getPrice() {
        return price;
    }

    public void setPrice(String price) {
        this.price = price;
    }
}
